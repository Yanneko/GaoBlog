     */
    public function store(CommentRequest $request)
    {
        $post = Post::findOrFail($request->post_id);
        $comment = new Comment;
        $comment -> body    = $request -> body;
        $comment -> user_id = Auth::id();
        $comment -> post_id = $request -> post_id;
